function onMouseOver() {
    var imgSettlement = document.getElementById("imgSettlement");
    var npcChiefImg = document.getElementById("npcChiefImg");
    imgSettlement.style.display="none";
    npcChiefImg.style.display="block";
}

function onMouseOut() {
    var imgSettlement = document.getElementById("imgSettlement");
    var npcChiefImg = document.getElementById("npcChiefImg");
    imgSettlement.style.display="block";
    npcChiefImg.style.display="none";

}